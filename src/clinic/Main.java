    private void table_displayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_displayMouseClicked
        int rowIndex = table_display.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) table_display.getModel();

        if (model.getValueAt(rowIndex, 4).toString().equals("Male")) {
            jRadioButton_m.setSelected(true);
            jRadioButton_f.setSelected(false);
        } else {
            jRadioButton_m.setSelected(false);
            jRadioButton_f.setSelected(true);
        }
        jTextField_id.setText(model.getValueAt(rowIndex, 0).toString());
        jTextField_adn.setText(model.getValueAt(rowIndex, 1).toString());
        jTextField_fn.setText(model.getValueAt(rowIndex, 2).toString());
        jTextField_ln.setText(model.getValueAt(rowIndex, 3).toString());
        jTextField_pn.setText(model.getValueAt(rowIndex, 6).toString());
        jTextField_a.setText(model.getValueAt(rowIndex, 7).toString());
        jTextField_nokn.setText(model.getValueAt(rowIndex, 8).toString());
        jTextField_nokc.setText(model.getValueAt(rowIndex, 9).toString());
        jTextField_search.setText(model.getValueAt(rowIndex, 1).toString());
        Date bdate;
        try {
            // bdate=new SimpleDateFormat("d-M-yyyy").parse(model.getValueAt(rowIndex, 5).toString());
            bdate = new SimpleDateFormat("MM/dd/yyyy").parse(model.getValueAt(rowIndex, 5).toString());
            jDateChooser.setDate(bdate);
        } catch (ParseException ex) {
            //Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_table_displayMouseClicked

