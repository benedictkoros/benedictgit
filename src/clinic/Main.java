    public boolean verifyText2() {
        if (jTextField_adn.getText().equals("") || jTextField_fn.getText().equals("") || jTextField_ln.getText().equals("") || jTextField_pn.getText().equals("")
                || jTextField_id.getText().equals("") || jTextField_adn.getText().equals("") || jTextField_nokn.getText().equals("") || jDateChooser.getDate() == null) {
            //  JOptionPane.showMessageDialog(null, "Empty");
            JOptionPane.showMessageDialog(null, "Empty fields are not allowed ");
            return false;
            //choose a date higher than the current one
        } else if (jDateChooser.getDate().compareTo(new Date()) > 0) {
            JOptionPane.showMessageDialog(null, "Error in Date of Birth");
            return false;
        } else {
            return true;
        }

    }
